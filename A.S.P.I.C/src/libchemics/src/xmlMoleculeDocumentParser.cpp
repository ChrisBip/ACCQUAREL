///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Method GET for the molecule
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
molecule moleculeDocumentParser::getMolecule4Document(const string & fileName)
{
	load(fileName);
	return getMolecule();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Method GET for the schema location.
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
string moleculeDocumentParser::getSchemaURI(void) const
{
	string schemaURI = aspicConfiguration::getMoleculeSchemaPath();
	return schemaURI;
}

